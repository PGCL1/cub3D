#include "../libft.h"

void	*ft_memcpy(void *dst, const void *src, size_t n)
{
	unsigned int long	i;

	i = 0;
	if (!src && !dst)
		return (NULL);
	if (src == dst)
		return (dst);
	while (i < n)
	{
		((unsigned char *)dst)[i] = ((unsigned char *)src)[i];
		i++;
	}
	return (dst);
}
