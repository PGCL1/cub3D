#include "../libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*temp;

	temp = lst;
	if (!lst)
		return (NULL);
	while (temp)
	{
		if (!(temp->next))
			return (temp);
		temp = temp->next;
	}
	return (temp);
}
